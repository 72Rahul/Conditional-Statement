
//WAP to check the number is even or odd.

#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"Enter the number : ";
    cin>>a;
   
    if(a%2==0){
        cout<<"The number is even\n";
    }
    else {
        cout<<"The number is odd\n";
    }
    return 0;
}